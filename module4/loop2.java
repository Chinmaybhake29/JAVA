package module4;

public class loop2 {

	public static void main(String[] args) {
		int i=1;
		System.out.println("ODD Number");
		while(i<=20)
		{
			System.out.println(i);
			i=i+2;
		}
		int i1=0;
		System.out.println("Even number");
		do
		{
			i1=i1+2;
			System.out.println(i1);
		}
		while(i1<=20);

	}

}
